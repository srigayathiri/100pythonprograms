# program to find area and  perimeter of a triangle

base = float(input("Enter the base : "))
height = float(input("Enter the height : "))

area = 1/2*(base*height)
print(f"Area of a triangle :{area}")

side1 = float(input("Enter the side1 : "))
side2 = float(input("Enter the side2 : "))
side3 = float(input("Enter the side3 : "))

perimeter = side1+side2+side3
print(f"Perimeter of a triangle :{perimeter}")
