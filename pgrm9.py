# program to convert celsius to fahrenheit and vice versa

celsius = float(input("Enter the degree in celsius : "))

conversion_celsius = (celsius * 9/5)+32

print(f"{celsius} degree celsius is equal to {conversion_celsius} degree fahrenheit")

